func newCollectorTargetMockServer(t *testing.T, collectorID, id string) (*httptest.Server, *atomic.Value) {
	var data atomic.Value
	server := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if r.Header.Get("Authorization") != "Bearer foo" {
			t.Fatal("Not authorized: " + r.Header.Get("Authorization"))
		}

		prefix := fmt.Sprintf("/api/v1/collectors/%s/targets", collectorID)

		switch {
		case r.Method == http.MethodPost && r.RequestURI == prefix:
			body, err := io.ReadAll(r.Body)
			if err != nil {
				t.Fatal(err)
			}
			// The real API does not return password/api_key — strip them on the create response.
			stripped := stripSecretsFromBody(t, body)
			stripped = inject(t, stripped, "status", "pending")
			stripped = inject(t, stripped, "autogenerated", false)
			stripped = inject(t, stripped, "enabled", true)
			stripped = inject(t, stripped, "created_at", "2026-05-19T10:00:00.000Z")
			stripped = inject(t, stripped, "updated_at", "2026-05-19T10:00:00.000Z")
			data.Store(stripped)
			w.WriteHeader(http.StatusCreated)
			_, _ = w.Write([]byte(fmt.Sprintf(`{"data":{"id":%q,"attributes":%s}}`, id, stripped)))

		case r.Method == http.MethodGet && r.RequestURI == prefix+"/"+id:
			_, _ = w.Write([]byte(fmt.Sprintf(`{"data":{"id":%q,"attributes":%s}}`, id, data.Load().([]byte))))

		case r.Method == http.MethodPatch && r.RequestURI == prefix+"/"+id:
			body, err := io.ReadAll(r.Body)
			if err != nil {
				t.Fatal(err)
			}
			patch := make(map[string]interface{})
			if err = json.Unmarshal(data.Load().([]byte), &patch); err != nil {
				t.Fatal(err)
			}
			if err = json.Unmarshal(body, &patch); err != nil {
				t.Fatal(err)
			}
			patched, err := json.Marshal(patch)
			if err != nil {
				t.Fatal(err)
			}
			patched = stripSecretsFromBody(t, patched)
			data.Store(patched)
			_, _ = w.Write([]byte(fmt.Sprintf(`{"data":{"id":%q,"attributes":%s}}`, id, patched)))

		case r.Method == http.MethodDelete && r.RequestURI == prefix+"/"+id:
			w.WriteHeader(http.StatusNoContent)
			data.Store([]byte(nil))

		default:
			t.Fatalf("Unexpected %s %s", r.Method, r.RequestURI)
		}
	}))
	return server, &data
}

func stripSecretsFromBody(t *testing.T, body []byte) []byte {
	m := make(map[string]interface{})
	if err := json.Unmarshal(body, &m); err != nil {
		t.Fatal(err)
	}
	delete(m, "password")
	delete(m, "api_key")
	out, err := json.Marshal(m)
	if err != nil {
		t.Fatal(err)
	}
	return out
}

