func TestResourceCollectorTargetDrift(t *testing.T) {
	// Simulates external edit: API returns host different from what TF set.
	var data atomic.Value

	server := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if r.Header.Get("Authorization") != "Bearer foo" {
			t.Fatal("Not authorized")
		}
		prefix := "/api/v1/collectors/77/targets"
		id := "5"
		switch {
		case r.Method == http.MethodPost && r.RequestURI == prefix:
			body, _ := io.ReadAll(r.Body)
			body = inject(t, body, "status", "pending")
			body = inject(t, body, "enabled", true)
			body = inject(t, body, "autogenerated", false)
			data.Store(body)
			w.WriteHeader(http.StatusCreated)
			_, _ = w.Write([]byte(fmt.Sprintf(`{"data":{"id":%q,"attributes":%s}}`, id, body)))
		case r.Method == http.MethodGet && r.RequestURI == prefix+"/"+id:
			// Simulate drift: someone changed the host externally.
			drifted := inject(t, data.Load().([]byte), "host", "externally-changed.example.com")
			_, _ = w.Write([]byte(fmt.Sprintf(`{"data":{"id":%q,"attributes":%s}}`, id, drifted)))
		case r.Method == http.MethodDelete && r.RequestURI == prefix+"/"+id:
			w.WriteHeader(http.StatusNoContent)
		case r.Method == http.MethodPatch:
			t.Fatal("unexpected PATCH")
		default:
			t.Fatalf("Unexpected %s %s", r.Method, r.RequestURI)
		}
	}))
	defer server.Close()

	resource.Test(t, resource.TestCase{
		IsUnitTest: true,
		ProviderFactories: map[string]func() (*schema.Provider, error){
			"logtail": func() (*schema.Provider, error) {
				return New(WithURL(server.URL)), nil
			},
		},
		Steps: []resource.TestStep{
			{
				Config: `
				provider "logtail" {
					api_token = "foo"
				}

				resource "logtail_collector_target" "db" {
					collector_id = "77"
					kind         = "redis"
					host         = "r.example.com"
					port         = 6379
				}
				`,
				// Post-apply refresh fetches drifted state from the API; replan must surface a non-empty diff.
				ExpectNonEmptyPlan: true,
			},
		},
	})
}

